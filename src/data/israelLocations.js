export const getAllCities = () => {
  return israelCities.map(city => city.name);
};

export const getCitiesByArea = (area) => {
  if (!area) return getAllCities();
  return israelCities
    .filter(city => city.area === area)
    .map(city => city.name);
};

export const getNeighborhoodsByCity = (cityName) => {
  return israelNeighborhoods[cityName] || [];
};

export const normalizeCity = (cityName) => {
  if (!cityName) return null;
  
  const city = israelCities.find(c => 
    c.name === cityName || 
    (c.nameVariants && c.nameVariants.includes(cityName))
  );
  
  return city ? city.name : cityName;
};

export const cityExists = (cityName) => {
  if (!cityName) return false;
  
  return israelCities.some(c => 
    c.name === cityName || 
    (c.nameVariants && c.nameVariants.includes(cityName))
  );
};

export const getCityInfo = (cityName) => {
  const normalizedName = normalizeCity(cityName);
  if (!normalizedName) return null;
  
  const city = israelCities.find(c => c.name === normalizedName);
  if (!city) return null;
  
  return {
    ...city,
    neighborhoods: getNeighborhoodsByCity(normalizedName)
  };
};

export const searchCities = (query) => {
  if (!query || query.length < 2) return getAllCities();
  
  return israelCities
    .filter(city => {
      if (city.name.includes(query)) return true;
      if (city.nameVariants && city.nameVariants.some(variant => 
        variant.includes(query)
      )) return true;
      return false;
    })
    .map(city => city.name);
};

export const getLocationStats = () => {
  const totalCities = israelCities.length;
  const citiesWithNeighborhoods = Object.keys(israelNeighborhoods).length;
  const totalNeighborhoods = Object.values(israelNeighborhoods)
    .reduce((total, neighborhoods) => total + neighborhoods.length, 0);
  
  const areaStats = israelAreas.map(area => ({
    area,
    cities: getCitiesByArea(area).length
  }));
  
  return {
    totalCities,
    citiesWithNeighborhoods,
    totalNeighborhoods,
    coveragePercentage: Math.round((citiesWithNeighborhoods / totalCities) * 100),
    areaStats
  };
};